def create_phone_number(n):
    phone = "("
    phone += str(n[0])
    phone += str(n[1])
    phone += str(n[2])
    phone += ")"
    phone += " "
    phone += str(n[3])
    phone += str(n[4])
    phone += str(n[5])
    phone += "-"
    phone += str(n[6])
    phone += str(n[7])
    phone += str(n[8])
    phone += str(n[9])
    return phone
